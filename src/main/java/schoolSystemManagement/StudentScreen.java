    /**
     * 사용자 정보를 라벨에 표시
     */
    private void displayUserData() {
        // 라벨에 사용자 정보 설정
        if (userData != null) {
            welcomeLabel.setText("환영합니다, " + userData.get("name") + "님!");
            idLabel.setText("아이디: " + userData.get("userId"));
            departmentLabel.setText("학과: " + userData.get("major"));
        } else {
            welcomeLabel.setText("사용자 정보 없음");
            idLabel.setText("");
            departmentLabel.setText("");
        }
    }

